# VARIABEL MASUKAN
a = 10
b = 20
c = 50

# MENGHITUNG NILAI DARI (a+b)*(b*c)/(a+b+c)
hasil1 = (a+b)*(b*c)/(a+b+c)
print("-----------------------------------------------")
print("HASIL NILAI (a+b)*(b*c)/(a+b+c)")
print(hasil1)
print("-----------------------------------------------")

# MENGHITUNG NILAI DARI P * L * t
hasil2 = a * b * c
